let count = 0;
for (const course of courses) {
  for (const level of course.levels) {
    for (const lesson of level.lessons) {
      const extra = theoryByLesson[lesson.id];
      if (extra) {
        lesson.theory = extra;
        count++;
      }
    }
  }
}

